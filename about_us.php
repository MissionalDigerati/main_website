						<p>Look around,  and you will notice that technology has saturated our world.  Computers, mobile phones, and tablet devices have become a natural part of our daily lives.  Currently there are over <a href="http://www.internetworldstats.com/stats.htm" rel="no-follow" target="_blank">2.26 billion internet users</a>, and <a href="http://en.wikipedia.org/wiki/List_of_countries_by_number_of_mobile_phones_in_use" rel="no-follow" target="_blank">5.6 billion mobile phone users</a> worldwide.  Even though these numbers may seem shocking,  they will only continue to rise throughout the following years.  If the Christian community could harness the power of these technologies,  we can dramatically expedite the delivery of the good news of Jesus Christ to every person on the Earth.  Missional Digerati is a Christian non-profit organization dedicated to leveraging technology for the purpose of delivering the Gospel to every nation.  As a web and mobile development team, we are focused on providing up-to-date technological expertise to existing Christian mission agencies, and developing tools that equip them for their unique task in the Great Commission.</p>
						
						<p>Missional Digerati is dedicated to the work of the greater Christian missional community.  Collaborating with a wide array of mission agencies, we evaluate the current needs of the community, and propose technology solutions that could solve those needs.  Being purposeful in all that we do, we only accept projects that impact areas of the world with the least amount of Christian representation. We then prioritize these projects based on the needs of the greater missional community.  With a fully supported staff, we are able to build these projects at little or no cost to the community.  Once a project is completed, we share the product freely with other agencies with in the community.  We also release the product under the <a href="http://www.gnu.org/licenses/" target="_blank" rel="no-follow">GNU General Public License</a>, insuring that our product will continue to be shared freely once it leaves our hands.</p>
						
